package estruturaFor;

public class EstruturaFor {
    public static void main(String[] args) {
        //for deve ser utilizado quando sabemos a quantidade de repetição

        //int i = 0 -> criando a variavel de cole
        //i<=10;-> condição de parada
        //i++ -> incremento da variável i
        for (int i = 0; i<=10; i++){
            System.out.println(i);
        }
    }
}
